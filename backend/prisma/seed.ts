async function main() {
  console.log('Seeding sources...');
  for (const source of sources) {
    const { en, fr, ...sourceData } = source;
    const upserted = await prisma.sources.upsert({
      where: { slug: source.slug },
      update: { cover_image_url: source.cover_image_url },
      create: sourceData,
    });
    // Upsert translations
    for (const [locale, title] of Object.entries({ en, fr })) {
      await prisma.source_translations.upsert({
        where: { source_id_locale: { source_id: upserted.id, locale } },
        update: { title },
        create: { source_id: upserted.id, locale, title },
      });
    }
  }
  console.log(`  ${sources.length} sources seeded.`);

  const allGrammarPoints = [
    ...n5GrammarPoints,
    ...n4GrammarPoints,
    ...n3GrammarPoints,
    ...n2GrammarPoints,
    ...n1GrammarPoints,
  ];
  console.log(`Seeding ${allGrammarPoints.length} grammar points...`);
  for (const gp of allGrammarPoints) {
    const { en, fr, notes, ...gpData } = gp as typeof gp & {
      en: string;
      fr: string;
      notes?: { en: string; fr: string };
    };
    const upserted = await prisma.grammar_points.upsert({
      where: { slug: gp.slug },
      update: { title: gp.title, romaji: gp.romaji, jlpt_level: gp.jlpt_level },
      create: gpData,
    });
    const meanings: Record<string, string> = { en, fr };
    for (const locale of ['en', 'fr']) {
      const meaning = meanings[locale];
      const localeNotes = notes?.[locale as 'en' | 'fr'] ?? null;
      await prisma.grammar_point_translations.upsert({
        where: { grammar_point_id_locale: { grammar_point_id: upserted.id, locale } },
        update: { meaning, notes: localeNotes },
        create: { grammar_point_id: upserted.id, locale, meaning, notes: localeNotes },
      });
    }
  }
  console.log(`  ${allGrammarPoints.length} grammar points seeded.`);

  console.log('Seeding speakers...');
  for (const speaker of speakers) {
    const { en_name, fr_name, ...speakerData } = speaker;
    const upserted = await prisma.speakers.upsert({
      where: { slug: speaker.slug },
      update: { name_japanese: speaker.name_japanese, image_url: speaker.image_url },
      create: speakerData,
    });
    for (const [locale, name] of Object.entries({ en: en_name, fr: fr_name })) {
      await prisma.speaker_translations.upsert({
        where: { speaker_id_locale: { speaker_id: upserted.id, locale } },
        update: { name },
        create: { speaker_id: upserted.id, locale, name },
      });
    }
  }
  console.log(`  ${speakers.length} speakers seeded.`);

  console.log('Seeding scenes...');
  for (const scene of scenes) {
    const { source_slug, transcript_lines: lines, ...sceneData } = scene;
    const source = await prisma.sources.findUniqueOrThrow({ where: { slug: source_slug } });
    let upsertedScene = await prisma.scenes.findFirst({
      where: {
        source_id: source.id,
        youtube_video_id: sceneData.youtube_video_id,
        start_time: sceneData.start_time,
      },
    });
    if (upsertedScene) {
      upsertedScene = await prisma.scenes.update({
        where: { id: upsertedScene.id },
        data: { end_time: sceneData.end_time, episode_number: sceneData.episode_number },
      });
    } else {
      upsertedScene = await prisma.scenes.create({
        data: { ...sceneData, source_id: source.id },
      });
    }

    for (const line of lines) {
      const { speaker_slug, translations, grammar_points: grammarPoints, ...lineData } = line;
      const speaker = await prisma.speakers.findUniqueOrThrow({ where: { slug: speaker_slug } });
      const upsertedLine = await prisma.transcript_lines.upsert({
        where: {
          scene_id_start_time_speaker_id: {
            scene_id: upsertedScene.id,
            start_time: lineData.start_time,
            speaker_id: speaker.id,
          },
        },
        update: { japanese_text: lineData.japanese_text },
        create: { ...lineData, scene_id: upsertedScene.id, speaker_id: speaker.id },
      });

      for (const [locale, translation] of Object.entries(translations)) {
        await prisma.transcript_line_translations.upsert({
          where: { transcript_line_id_locale: { transcript_line_id: upsertedLine.id, locale } },
          update: { translation },
          create: { transcript_line_id: upsertedLine.id, locale, translation },
        });
      }

      for (const gp of grammarPoints) {
        const grammarPoint = await prisma.grammar_points.findUniqueOrThrow({
          where: { slug: gp.slug },
        });
        const existingGpLink = await prisma.transcript_line_grammar_points.findFirst({
          where: {
            transcript_line_id: upsertedLine.id,
            grammar_point_id: grammarPoint.id,
            start_index: gp.start_index,
            end_index: gp.end_index,
          },
        });
        if (!existingGpLink) {
          await prisma.transcript_line_grammar_points.create({
            data: {
              transcript_line_id: upsertedLine.id,
              grammar_point_id: grammarPoint.id,
              start_index: gp.start_index,
              end_index: gp.end_index,
              matched_form: gp.matched_form,
            },
          });
        }
      }
    }
  }
  console.log(`  ${scenes.length} scenes seeded.`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
